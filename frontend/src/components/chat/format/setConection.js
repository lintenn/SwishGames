import React from 'react';

export const setConection = ( receptor, users, setConexion ) => {

  users.forEach( ( us ) => {

    if ( us.nombre === receptor ) {

      if ( us.online ) {

        setConexion(
          <div id="divOnline">
            <div id="online"></div>
              Online
          </div> );

      } else {

        setConexion(
          <div id="divOffline">
            <div id="offline"></div>
              Offline
          </div> );

      }

    }

  });

};
